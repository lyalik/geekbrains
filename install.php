Чтобы использовать коммиты, нужно представить Гиту. Для "авторизации" в Гите потребуется ввести имя и почту в терминале VSC (куда мы и вводим команду "git init").
Представиться лучше перед тем, как сделали первый коммит.

Для ввода имени используем: git config --global user.name “Your_name”
Для ввода почты используем: git config --global user.email ваша почта@example.com

Вставляем свое имя и почту, к примеру:
git config --global user.name “Mikhail”
git config --global user.email msmerkushov123@yandex.ru